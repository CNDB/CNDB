_ffm_clean = Multi_Re_Replacer \
    ( Re_Replacer
        ( r"`net_address` \[.*$"
        , "`net_address` ..."
        , re.MULTILINE
        )
    , Re_Replacer
        ( r"^(\s+)[a-z0-9_]+\.net_address__.*$" "\n"
        , r""
        , re.MULTILINE
        )
    , Re_Replacer
        ( r"^(\s+)[a-zA-Z_]+ net_address__.*$" "\n"
        , r""
        , re.MULTILINE
        )
    , Re_Replacer
        ( r"^(\s+Column net_address\s+: )(\w+\s+)"
        , r"\1CIDR                 "
        , re.MULTILINE
        )
    )

@TFL.Decorator
def _cleaned (f) :
    def _ (* args, ** kw) :
        buffer = pyk.StringIO ()
        with attr_let (sys, stdout = buffer) :
            f (* args, ** kw)
        result  = buffer.getvalue ()
        cleaned = _ffm_clean (result)
        print (cleaned)
    return _

